    def build_field_value_generic2(self, name, obj, value):
        if type(obj) in [str, int, float, bool]:
            value[name] = obj
        elif type(obj) == list:
            l = len(obj)
            if name == "prestationIds":
                if l == 1:
                    self.build_field_value_generic2("last " + name, obj[l - 1], value)
                elif l == 2:
                    self.build_field_value_generic2("last " + name, obj[l - 1], value)
                    self.build_field_value_generic2("last " + name + " 2", obj[l - 2], value)
                elif l >= 3:
                    self.build_field_value_generic2("last " + name, obj[l - 1], value)
                    self.build_field_value_generic2("last " + name + " 2", obj[l - 2], value)
                    self.build_field_value_generic2("last " + name + " 3", obj[l - 3], value)
            else:
                self.build_field_value_generic2("last " + name, obj[l - 1], value)
        elif type(obj) == dict:
            for it in obj:
                self.build_field_value_generic2(name + " " + str(it), obj[it], value)
        else:
            value[name] = 'NULL'
